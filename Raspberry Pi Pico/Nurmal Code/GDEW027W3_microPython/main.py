#SPI#####################
def SPI_Write(data):
    spi.write(chr(data))		

def EPD_W21_WriteCMD(command):
    EPD_W21_CS.value(0) #CS                
    EPD_W21_DC.value(0) #DC		
    SPI_Write(command)# command write
    EPD_W21_CS.value(1) #CS  

def EPD_W21_WriteDATA(data):
    EPD_W21_CS.value(0) #CS                       
    EPD_W21_DC.value(1) #DC			
    SPI_Write(data)  # data write
    EPD_W21_CS.value(1) #CS  

#EPD IC RESET
def EPD_W21_Init():
    EPD_W21_RST.value(0) # Module reset
    utime.sleep(0.01)   #At least 10ms delay 
    EPD_W21_RST.value(1)
    utime.sleep(0.01)   #At least 10ms delay 

#BUSY
def lcd_chkstatus():
    while isEPD_W21_BUSY.value()==0: #0 is busy
        utime.sleep(0.01) 
#UC8151D init
def EPD_init():
    EPD_W21_Init()  #Electronic paper IC reset

    EPD_W21_WriteCMD(0x06)         #boost soft start
    EPD_W21_WriteDATA (0x17)		#A
    EPD_W21_WriteDATA (0x17)		#B
    EPD_W21_WriteDATA (0x17)		#C       

    EPD_W21_WriteCMD(0x04)  
    lcd_chkstatus()

    EPD_W21_WriteCMD(0x00)			#panel setting
    EPD_W21_WriteDATA(0x1f)		#LUT from OTP£-264*176

    EPD_W21_WriteCMD(0x16)
    EPD_W21_WriteDATA(0x00)				#KW-BF   KWR-AF	BWROTP 0f	

    EPD_W21_WriteCMD(0xF8)         #boostÉè¶¨
    EPD_W21_WriteDATA (0x60)
    EPD_W21_WriteDATA(0xa5)

    EPD_W21_WriteCMD(0xF8)         #boostÉè¶¨
    EPD_W21_WriteDATA (0x73)
    EPD_W21_WriteDATA (0x23)

    EPD_W21_WriteCMD(0xF8)         #boostÉè¶¨
    EPD_W21_WriteDATA (0x7C)
    EPD_W21_WriteDATA(0x00)
    
    EPD_W21_WriteCMD(0X50)
    EPD_W21_WriteDATA(0x97)		#WBmode:VBDF 17|D7 VBDW 97 VBDB 57		WBRmode:VBDF F7 VBDW 77 VBDB 37  VBDR B7#display 

def PIC_display(picData):
    #Write Data
    EPD_W21_WriteCMD(0x10)       #Transfer old data
    for i in range(5808):
        EPD_W21_WriteDATA(0xff)

    EPD_W21_WriteCMD(0x13)		     #Transfer new data
    for i in range(5808):     
        EPD_W21_WriteDATA(picData[i]) #Transfer the actual displayed data

    #Refresh
    EPD_W21_WriteCMD(0x12)		#DISPLAY REFRESH 	
    utime.sleep(0.01)	        #!!!The delay here is necessary, 200uS at least!!! 
    lcd_chkstatus()          #waiting for the electronic paper IC to release the idle signal
#Clear screen
def PIC_display_Clear():
    #Write Data
    EPD_W21_WriteCMD(0x10)	       #Transfer old data
    for i in range(5808):  	
        EPD_W21_WriteDATA(0xFF) 
        EPD_W21_WriteCMD(0x13)		     #Transfer new data
    for i in range(5808):       
        EPD_W21_WriteDATA(0xFF)  #Transfer the actual displayed data
    #Refresh
    EPD_W21_WriteCMD(0x12)		#DISPLAY REFRESH 	
    utime.sleep(0.01)	             #!!!The delay here is necessary, 200uS at least!!!     
    lcd_chkstatus()         #waiting for the electronic paper IC to release the idle signal
#Enter deep sleep mode 
def EPD_sleep(): 
    EPD_W21_WriteCMD(0X50)  #VCOM AND DATA INTERVAL SETTING			
    EPD_W21_WriteDATA(0xf7) #WBmode:VBDF 17|D7 VBDW 97 VBDB 57		WBRmode:VBDF F7 VBDW 77 VBDB 37  VBDR B7	

    EPD_W21_WriteCMD(0X02)  	#power off
    lcd_chkstatus()          #waiting for the electronic paper IC to release the idle signal
    EPD_W21_WriteCMD(0X07)  	#deep sleep
    EPD_W21_WriteDATA(0xA5)
 
 
